/**
 * GRADE CALCULATOR
 */
var marks= 50;

if((90<=marks) && (100>=marks)){
    console.log('A');
}

else if((80<=marks) && (89>=marks)){
    console.log('B');
}

else if((70<=marks) && (79>=marks)){
    console.log('C');
}

else if((60<=marks) && (69>=marks)){
    console.log('D');
}

else{
    console.log('F');
}